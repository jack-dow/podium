// Template Sets
/** Returns all the template sets. **NOTE: Not in position order** */
export function useTemplateSetIds() {
  const existingTemplateSets = useTemplateContext((s) => s.templateSet.existing);
  const newTemplateSets = useTemplateContext((s) => s.templateSet.new);
  const updatedTemplateSets = useTemplateContext((s) => s.templateSet.updated);

  return [...existingTemplateSets, ...Object.keys(newTemplateSets), ...Object.keys(updatedTemplateSets)];
}

/** Returns the template sets for the given template exercise id in order of their position */
export function useTemplateSetIdsByTemplateExerciseId(templateExerciseId: TemplateExerciseId): TemplateSetId[] | null {
  const positions = useTemplateContext((s) => s.templateSet.positions[templateExerciseId] ?? null);
  if (!positions) return null;
  const swapped = Object.entries(positions).map(([key, value]) => [value, key]);
  const ids = swapped.sort().map(([_, value]) => value as string);
  return ids;
}

/** Returns the full template set or null if that id does not exist  */
export function useTemplateSet(templateSetId: TemplateSetId) {
  const updateTemplateSet = useTemplateContext((s) => s.api.updateTemplateSet);
  const setTemplateSet = (changes: ChangeableTemplateSetProperties) => updateTemplateSet(templateSetId, changes);
  const newTemplateSet: NewTemplateSet | null = useTemplateContext((s) => s.templateSet.new[templateSetId] ?? null);
  const updatedTemplateSet: UpdatedTemplateSet | null = useTemplateContext(
    (s) => s.templateSet.updated[templateSetId] ?? null,
  );

  const {
    data: templateSet,
    isLoading: isTemplateSetLoading,
    isError: isTemplateSetError,
  } = trpc.templateSet.byId.useQuery({ id: templateSetId }, { enabled: !updatedTemplateSet && !newTemplateSet });

  if (!updatedTemplateSet && !newTemplateSet) {
    return {
      templateSet,
      isLoading: isTemplateSetLoading,
      isError: isTemplateSetError,
      setTemplateSet,
    };
  }

  if (newTemplateSet || updatedTemplateSet) {
    return {
      templateSet: {
        ...newTemplateSet,
        ...updatedTemplateSet,
      },
      isLoading: false,
      isError: false,
      setTemplateSet,
    };
  }

  return { templateSet: null, isLoading: false, isError: false, setTemplateSet: () => {} };
}

/** Returns all the necessary information about the template and its exercises and sets for submission  */
export function useTemplateSubmissionInformation() {
  const userId = useAuthSession()?.user.id;

  if (!userId) {
    throw new Error(
      '[useTemplateSubmissionInformation] Hook was called without a user being logged in. Please fix this.',
    );
  }

  const templateId = useTemplateContext((s) => s.id);
  const templateName = useTemplateContext((s) => s.name);

  const templateExercisePositions = useTemplateContext((s) => s.templateExercise.positions);

  const templateExerciseNew = useTemplateContext((s) => s.templateExercise.new);
  const submittableTemplateExerciseNew: Record<TemplateExerciseId, Omit<TemplateExercise, 'createdAt'>> = {};
  for (const templateExerciseId in templateExerciseNew) {
    submittableTemplateExerciseNew[templateExerciseId] = {
      ...templateExerciseNew[templateExerciseId],
      position: templateExercisePositions[templateExerciseId],
      userId,
    };
  }

  const templateExerciseUpdated = useTemplateContext((s) => s.templateExercise.updated);
  const templateExerciseDeleted = useTemplateContext((s) => s.templateExercise.deleted);

  const templateSetPositions = useTemplateContext((s) => s.templateSet.positions);
  const templateSetNew = useTemplateContext((s) => s.templateSet.new);
  const submittableTemplateSetNew: Record<TemplateSetId, Omit<TemplateSet, 'createdAt'>> = {};
  for (const templateSetId in templateSetNew) {
    const templateSet = templateSetNew[templateSetId];
    submittableTemplateSetNew[templateSetId] = {
      ...templateSet,
      position: templateSetPositions[templateSet.templateExerciseId][templateSetId],
      userId,
    };
  }
  const templateSetUpdated = useTemplateContext((s) => s.templateSet.updated);
  const templateSetDeleted = useTemplateContext((s) => s.templateSet.deleted);

  return {
    id: templateId,
    name: templateName,

    templateExercise: {
      new: submittableTemplateExerciseNew,
      updated: templateExerciseUpdated,
      deleted: templateExerciseDeleted,
    },

    templateSet: {
      new: submittableTemplateSetNew,
      updated: templateSetUpdated,
      deleted: templateSetDeleted,
    },
  };
}
