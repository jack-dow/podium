/** Returns an array of all the template exercise ids in order of their positions */
export function useTemplateExerciseIds() {
  const positions = useTemplateContext((s) => s.templateExercise.positions);
  const swapped = Object.entries(positions).map(([key, value]) => [value, key]);
  const ids = swapped.sort().map(([_, value]) => value as string);
  return ids;
}
/** Returns all the given template sets that relate to the provided exercise id */
export function useTemplateExercisesIdsByExerciseId(exerciseId: ExerciseId): TemplateExerciseId[] | null {
  const templateExercises = useTemplateContext((s) => s.templateExercise.byExerciseId[exerciseId] ?? null);
  return templateExercises;
}

/** Returns the full template exercise of the given template exercise id or null if that id does not exist */
export function useTemplateExercise(templateExerciseId: TemplateExerciseId) {
  const newTemplateExercise: NewTemplateExercise | null = useTemplateContext(
    (s) => s.templateExercise.new[templateExerciseId] ?? null,
  );
  const updatedTemplateExercise: UpdatedTemplateExercise | null = useTemplateContext(
    (s) => s.templateExercise.updated[templateExerciseId] ?? null,
  );

  const {
    data: templateExercise,
    isLoading: isTemplateExerciseLoading,
    isError: isTemplateExerciseError,
  } = trpc.templateExercise.byId.useQuery(
    { id: templateExerciseId },
    { enabled: !updatedTemplateExercise && !newTemplateExercise },
  );

  const {
    data: exercise,
    isLoading: isExerciseLoading,
    isError: isExerciseError,
  } = trpc.exercise.byId.useQuery(
    { id: newTemplateExercise.exerciseId || updatedTemplateExercise.exerciseId },
    { enabled: !!newTemplateExercise || !!updatedTemplateExercise },
  );

  if (!updatedTemplateExercise && !newTemplateExercise) {
    return {
      templateExercise,
      isLoading: isTemplateExerciseLoading,
      isError: isTemplateExerciseError,
    };
  }

  return {
    templateExercise: {
      ...newTemplateExercise,
      ...updatedTemplateExercise,
      exercise,
    },
    isLoading: isExerciseLoading,
    isError: isExerciseError,
  };
}

