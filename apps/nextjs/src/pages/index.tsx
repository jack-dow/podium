import type { NextPage } from 'next';
import Head from 'next/head';
import type { inferProcedureOutput } from '@trpc/server';
import type { AppRouter } from '@podium/api';
import { trpc } from '../utils/trpc';
import { withAuth } from '@/components/auth/WithAuth';

const ExerciseCard: React.FC<{
  exercise: inferProcedureOutput<AppRouter['exercises']['all']>[number];
}> = ({ exercise }) => {
  return (
    <div className="p-4 ">
      <h2 className="text-gray-800 text-2xl font-bold">{exercise.name}</h2>
      <p className="text-gray-600">{exercise.category}</p>
    </div>
  );
};

const Home: NextPage = () => {
  const exerciseQuery = trpc.exercises.all.useQuery();

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="container mx-auto flex min-h-screen flex-col items-center justify-center p-base">
        <h1 className="text-gray-700 text-5xl font-extrabold leading-normal md:text-[5rem]">
          Create <span className="text-purple-300">T3</span> App
        </h1>
        <div className="pt-6 text-blue-500 flex w-full items-center justify-center text-2xl">
          {exerciseQuery.data ? (
            <div className="gap-4 flex flex-col">
              {exerciseQuery.data?.map((p) => {
                return <ExerciseCard key={p.id} exercise={p} />;
              })}
            </div>
          ) : (
            <p>Loading..</p>
          )}
        </div>
      </main>
    </>
  );
};

export default Home;
